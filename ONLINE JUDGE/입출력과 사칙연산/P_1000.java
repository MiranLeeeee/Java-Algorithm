package 입출력과사칙연산;

import java.util.Scanner;

/*[1000] A+B
1. 문제 및 제약조건
:	두 정수 A와 B를 입력받은 다음, A+B를 출력하는 프로그램을 작성하시오.
2. 입력
:	첫째 줄에 A와 B가 주어진다. (0 < A, B < 10)
3. 출력
:	첫째 줄에 A+B를 출력한다.
*/

public class P_1000 {
	
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int a = sc.nextInt();
		int b = sc.nextInt();
		System.out.println(a+b);
		sc.close();
	}

}
